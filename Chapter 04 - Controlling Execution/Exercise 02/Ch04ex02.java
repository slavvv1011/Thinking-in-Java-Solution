package ch04ex02;
import java.util.*;

public class Ch04ex02 {

	public static void main(String[] args) {
        Random rand = new Random(256);
        for(int i = 0; i < 25; i++){
            int j = rand.nextInt();
            int k = rand.nextInt();
            if(j < k)
                System.out.println("j = " + j + ", k = " + k + ", j < k");
            else if(j > k)
                System.out.println("j = " + j + ", k = " + k + ", j > k");
            else
                System.out.println("j = " + j + ", k = " + k + ", j = k");
        }
    }
}

